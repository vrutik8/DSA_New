import java.util.*;

public class Operator{
    public static void main(String args[]){
        int no1 = 30;
        int no2 = 3;
        System.out.println("no1 and no2 is"+ (no1 + no2));
        System.out.println("no1 and no2 is"+ (no1 - no2));
        System.out.println("no1 and no2 is"+ (no1 / no2));
        System.out.println("no1 and no2 is"+ (no1 * no2));
        System.out.println("no1 and no2 is"+ (no1 % no2));
    }
}