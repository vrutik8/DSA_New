import java.util.*;

public class Relationaloperator{
    public static void main(String args[]){
        int a = 10;
        int b = 10;
        System.out.println(a==b);
        int c = 10;
        int d = 10;
        System.out.println(c!=d);
        int e = 10;
        int f = 11;
        System.out.println(f>e);
        int g = 10;
        int h = 11;
        System.out.println(g<h);
        int i = 10;
        int j = 10;
        System.out.println(i<=j);
        int k = 10;
        int l = 10;
        System.out.println(k>=l);
    }
}
