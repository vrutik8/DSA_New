import java.util.*;

public class nininswitchcase{
    public static void main(String args[]){
        char ch = 'c';
        switch(ch){
            case 'a':
            System.out.println("mango");
            break;
            case 'b':
            System.out.println("Orange");
            break;
            case 'c':
            System.out.println("papayaa");
            break;
            default:
            System.out.println("check it");
        }
    }

}